CREATE TABLE MEMBER(
        M_ID NUMBER(5) PRIMARY KEY,
        M_NAME VARCHAR2(30) NOT NULL,
        M_ADD VARCHAR2(50),
        A_O_D DATE,
        MEMBERSHIP_TYPE VARCHAR2(30),
        FEES NUMBER(5),
        MAX_BOOK NUMBER(2),
        PENALTY NUMBER(7,2)
        );
        
