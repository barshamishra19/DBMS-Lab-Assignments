CREATE TABLE MEMBER(
        M_ID NUMBER(5) PRIMARY KEY,
        M_NAME VARCHAR2(30) NOT NULL,
        M_ADD VARCHAR2(50),
        A_O_D DATE,
        MEMBERSHIP_TYPE VARCHAR2(30),
        FEES NUMBER(5),
        MAX_BOOK NUMBER(2),
        PENALTY NUMBER(7,2)
        );
        
CREATE TABLE BOOKS(
    B_NO NUMBER(6),
    B_NAME VARCHAR2(30),
    A_NAME VARCHAR2(30),
    COST NUMBER(7,2),
    CAT CHAR(10)
    );

CREATE TABLE ISSUE_NEW(
     LIB_ISSUE_ID NUMBER(10),
     B_NO NUMBER(6),
     M_ID NUMBER(5),
     ISSUE_DATE DATE,
     RETURN_DATE DATE
     );
    
    
